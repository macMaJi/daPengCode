public class RecycleObject {

    private String autoCleanRemain;
    private String fileDeleteTime;
    private int checked;

    public String getAutoCleanRemain() {
        return autoCleanRemain;
    }

    public void setAutoCleanRemain(String autoCleanRemain) {
        this.autoCleanRemain = autoCleanRemain;
    }

    public String getFileDeleteTime() {
        return fileDeleteTime;
    }

    public void setFileDeleteTime(String fileDeleteTime) {
        this.fileDeleteTime = fileDeleteTime;
    }

    public int getChecked() {
        return checked;
    }

    public void setChecked(int checked) {
        this.checked = checked;
    }
}
